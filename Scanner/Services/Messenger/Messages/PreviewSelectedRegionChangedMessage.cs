using Microsoft.Toolkit.Mvvm.Messaging.Messages;
using Windows.Foundation;
using static Enums;

namespace Scanner.Services.Messenger
{
    class PreviewSelectedRegionChangedMessage : ValueChangedMessage<Size?>
    {
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // DECLARATIONS /////////////////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public Size? SelectedRegion;


        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // CONSTRUCTORS / FACTORIES /////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public PreviewSelectedRegionChangedMessage(Size? selectedRegion) : base(selectedRegion)
        {
            SelectedRegion = selectedRegion;
        }
    }
}
